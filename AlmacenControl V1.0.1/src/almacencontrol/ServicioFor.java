    public void guardaServicio() {
        int op = JOptionPane.showInternalConfirmDialog(super.getContentPane(), "Deceas Guardar el Servicio?");
        if (op == JOptionPane.OK_OPTION) {
            String Fecha = principalAlmacen.GetFecha();
            String Hora = principalAlmacen.getHora();
            ConexionMySQL mysql = new ConexionMySQL();
            Connection cn = mysql.Conectar();
            PreparedStatement pst;
            String sSQL = "";
            String mensaje = "Servicio Guardado con Exito!!!";
            String cTr = jComboBox1.getSelectedItem().toString();
            int nCTr = Integer.parseInt(jTextField3.getText());
            int nSol = Integer.parseInt(jTextField1.getText());
            int idusuario = principalAlmacen.usr;
            float tot = Float.parseFloat(jTextField6.getText());
            String descrip = jTextArea1.getText();

            /*
             idUsr int,
             fecha date,
             hora time,
             concepto varchar(1000),
             remison int,
             CaTra varchar(20),
             nCaTra int,
             total numeric(8,2),
             */
            sSQL = "INSERT INTO  servicioForaneo (idUsr, fecha, hora, concepto, remison, CaTra, nCaTra, total)"
                    + "VALUES(?, ?, ?, ?, ?, ?, ?, ?)";

            try {
                pst = cn.prepareStatement(sSQL);

                pst.setInt(1, idusuario);
                pst.setString(2, Fecha);
                pst.setString(3, Hora);
                pst.setString(4, descrip);
                pst.setInt(5, nSol);
                pst.setString(6, cTr);
                pst.setInt(7, nCTr);
                pst.setFloat(8, tot);
                int n = pst.executeUpdate();
                if (n > 0) {
                    JOptionPane.showMessageDialog(null,mensaje);
                    this.dispose();
                }

            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
        }
    }


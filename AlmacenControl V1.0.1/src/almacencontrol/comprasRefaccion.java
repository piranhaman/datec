    public static void calcular() {
        DefaultTableModel m = (DefaultTableModel) prod.getModel();
        int filas = prod.getRowCount();
        total = 0;
        for (int i = 0; i < filas; i++) {

            float pr = (Integer.parseInt(m.getValueAt(i, 4).toString())) * (Float.parseFloat(m.getValueAt(i, 3).toString()));
            m.setValueAt(pr, i, 5);
            total += Float.parseFloat(m.getValueAt(i, 5).toString());

        }

        jLabel7.setText(String.format("%.2f", total));

    }


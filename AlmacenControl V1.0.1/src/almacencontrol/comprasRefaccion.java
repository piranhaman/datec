    public void guardaCompra() {
        Date fecha = jDateChooser1.getDate();
        SimpleDateFormat formato = new SimpleDateFormat("yyyy-MM-dd");
        String Fech = formato.format(fecha);

        ConexionMySQL mysql = new ConexionMySQL();
        Connection cn = mysql.Conectar();
        PreparedStatement pst;
        String sSQL;
        String mensaje;
        int errores=1;

        int idProd = 0, cant = 0, idusuario = principalAlmacen.usr;;
        float costo = 0, tot = 0;
        String destino = jComboBox1.getSelectedItem().toString();
        int fact = Integer.parseInt(jTextField1.getText());
        int nCaTra = 0;
        float total = Float.parseFloat(jLabel7.getText());
        if (!destino.equals("Almacen")) {
            nCaTra = Integer.parseInt(jTextField3.getText());
        }

        sSQL = "INSERT INTO  compra (idUsr,remison,fecha,destino,nCaTra, idRef,cantidad, total,totalRem)"
                + "VALUES(?, ?, ?, ?, ?, ?, ?, ?, ?)";
        mensaje = "Compra realizada con Exito!!!";

        DefaultTableModel m = (DefaultTableModel) prod.getModel();

        for (int i = 0; i < m.getRowCount(); i++) {

            idProd = getIdProd(m.getValueAt(i, 0).toString());
            cant = Integer.parseInt(m.getValueAt(i, 4).toString());
            costo = Float.parseFloat(m.getValueAt(i, 3).toString());
            tot = cant * costo;

            try {
                pst = cn.prepareStatement(sSQL);

                /*
                 idUsr int,
                 remison int,
                 fecha date,
                 destino varchar(30),
                 nCaTra int,
                 idRef int,
                 cantidad int,
                 total numeric(8,2),
                 totalRem numeric(8,2),
                 */
                pst.setInt(1, idusuario);
                pst.setInt(2, fact);
                pst.setString(3, Fech);
                pst.setString(4, destino);
                pst.setInt(5, nCaTra);
                pst.setInt(6, idProd);
                pst.setInt(7, cant);
                pst.setFloat(8, tot);
                pst.setFloat(9, total);
                int n = pst.executeUpdate();
                if (n > 0) {
                    errores=errores*1;
                }else{
                    errores=errores*0;
                }

            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
        }
        if(errores==1){
            JOptionPane.showMessageDialog(null, mensaje);
                    this.dispose();
        }

    }

    public int getIdProd(String code) {
        int id = 0;

        ConexionMySQL conect = new ConexionMySQL();
        String consulta = "select * from refacciones where codigo='" + code + "'";
        ResultSet rs = conect.Consulta(consulta);
        try {
            while (rs.next()) {
                id = rs.getInt("id");
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Error al cargar proovedores!!!");
        }

        return id;
    }


    public int getUsuario(String nombre) {
        int n = 1;

        ConexionMySQL conect = new ConexionMySQL();
        String consulta = "select * from usuarios where nombre='" + nombre + "'";
        ResultSet rs = conect.Consulta(consulta);
        try {
            while (rs.next()) {
                n = rs.getInt("id");
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Error!!!... "+ex);
        }
        return n;
    }
    
     
    public static String GetFecha() {

        Calendar fecha = new GregorianCalendar();
        int anio = fecha.get(Calendar.YEAR);
        int mes = fecha.get(Calendar.MONTH) + 1;
        int dia = fecha.get(Calendar.DAY_OF_MONTH);
        String fech = anio + "-" + mes + "-" + dia;

        return fech;

    }

    public static String getHora() {

        String hora = "";
        Calendar fecha = new GregorianCalendar();
        int horas = fecha.get(Calendar.HOUR);
        int min = fecha.get(Calendar.MINUTE);
        if (horas < 10) {
            hora = "0" + horas + ":";
        } else {
            hora = horas + ":";
        }
        if (min < 10) {
            hora = hora + "0" + min + ":00";
        } else {
            hora = hora + min + ":00";
        }

        return hora;
    }
    

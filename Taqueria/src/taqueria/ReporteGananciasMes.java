    public int getAnio() {
        Calendar fecha = new GregorianCalendar();
        int anio = fecha.get(Calendar.YEAR);
        jComboBox1.removeAllItems();
        jComboBox1.addItem("" + (anio - 3));
        jComboBox1.addItem("" + (anio - 2));
        jComboBox1.addItem("" + (anio - 1));
        jComboBox1.addItem("" + (anio));
        jComboBox1.setSelectedItem("" + (anio));
        return anio;
    }

    public int getMes() {
        Calendar fecha = new GregorianCalendar();
        int mes = fecha.get(Calendar.MONTH);
        jComboBox2.setSelectedIndex(mes);
        return mes + 1;
    }



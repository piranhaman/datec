    public boolean dupli(String code) {
        boolean a = false;

        ConexionMySQL conect = new ConexionMySQL();
        String consulta = "select * from productosEmpaquetados where Nombre='" + code + "'";
        ResultSet rs = conect.Consulta(consulta);
        try {
            while (rs.next()) {
                a = true;
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Error al cargar proovedores!!!");
        }

        return a;

    }

